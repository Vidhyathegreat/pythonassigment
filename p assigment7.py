list=[1,2,3,4,5]

#append#
list.append(6)
print(list)

#copy#
list.copy()
print(list)

#count#
x=list.count(1)
print(x)

#extend#
l1=[7,8,9,10,11]
list.extend(l1)
print(list)

#index#
y=list.index(3)
print(y)

#insert#
list.insert(2,12)
print(list)

#pop#
list.pop(3)
print(list)

#remove#
list.remove(5)
print(list)

#reverse#
list.reverse()
print(list)

#sort#
list.sort()
print(list)

#clear#
list.clear()
print(list)
